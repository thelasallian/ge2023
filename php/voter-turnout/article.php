    <p>
        Voter population for both Manila and Laguna campuses has significantly grown
        in recent years, yet voter turnouts remain around or barely exceed the 
        50-percent mark required to render a valid election. In the past five years, 
        the highest election turnout was 60.02 percent, during the 
        <a href="https://thelasallian.com/2021/02/06/make-up-elections-2021-end-in-split-charter-revisions-finally-ratified/">2021 Make-Up Elections</a>, while the most recent 
        <a href="https://thelasallian.com/2022/12/05/tapat-seizes-back-usg-in-2022-make-up-elections/">2022 Make-Up Elections</a> was scarcely 
        validated at 51.83 percent even with a two-day extension.
    </p>

    <p>
        The problem with the lack of voting participation from the student body 
        has been a longstanding problem for the USG. Back during the 
        <a href="https://thelasallian.com/2016/08/07/a-year-in-review-the-usg-of-ay-2015-2016/">2015 General Elections</a>, 
        DLSU Comelec was forced to hold off the start of office until the second 
        term of the academic year due to a plebiscite failure after receiving a 
        voter turnout of 35.90 percent, well below the 50 percent plus one 
        requirement.
    </p>

